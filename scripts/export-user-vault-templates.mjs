#!/usr/bin/env node
/**
 * Reads tags + categories for a Firestore user and prints TypeScript for
 * `src/data/defaultVaultTemplates.ts`, or writes it with --write.
 *
 * Usage:
 *   node scripts/export-user-vault-templates.mjs --uid=FIREBASE_UID
 *   node scripts/export-user-vault-templates.mjs --uid=FIREBASE_UID --write
 *
 * Requires scripts/service-account.json or GOOGLE_APPLICATION_CREDENTIALS.
 */

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { initializeApp, cert } from "firebase-admin/app";
import { getFirestore } from "firebase-admin/firestore";

const args = process.argv.slice(2);
const uidArg = args.find((a) => a.startsWith("--uid="));
const uid = uidArg?.slice("--uid=".length)?.trim();
const doWrite = args.includes("--write");

if (!uid) {
  console.error("Required: --uid=FIREBASE_AUTH_UID");
  process.exit(1);
}

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const saPath =
  process.env.GOOGLE_APPLICATION_CREDENTIALS ||
  path.resolve(scriptDir, "service-account.json");

if (!fs.existsSync(saPath)) {
  console.error(`Service account key not found: ${saPath}`);
  process.exit(1);
}

const serviceAccount = JSON.parse(fs.readFileSync(saPath, "utf-8"));
initializeApp({ credential: cert(serviceAccount) });
const db = getFirestore();
db.settings({ preferRest: true });

const [tagSnap, catSnap] = await Promise.all([
  db.collection("tags").where("ownerId", "==", uid).orderBy("name").get(),
  db.collection("categories").where("ownerId", "==", uid).orderBy("name").get(),
]);

const tags = tagSnap.docs
  .map((d) => {
    const x = d.data();
    return {
      name: String(x.name ?? ""),
      color: String(x.color ?? "#78716c"),
      category: String(x.category ?? "Other"),
    };
  })
  .filter((t) => !t.name.includes("\uFFFD"));

const categories = catSnap.docs.map((d) => {
  const x = d.data();
  return {
    name: String(x.name ?? ""),
    description: String(x.description ?? ""),
    icon: String(x.icon ?? "utensils"),
  };
});

const header = `/** Generated by scripts/export-user-vault-templates.mjs from ownerId ${uid} — edit freely. */

`;

const tagsBlock = `/** Starter tags copied into each new account (owned by the user; fully editable). */
export const DEFAULT_TAGS: ReadonlyArray<{
  name: string;
  color: string;
  category: string;
}> = [
${tags.map((t) => `  { name: ${JSON.stringify(t.name)}, color: ${JSON.stringify(t.color)}, category: ${JSON.stringify(t.category)} },`).join("\n")}
];
`;

const catsBlock = `
/** Starter recipe categories copied into each new account (owned by the user; fully editable). */
export const DEFAULT_CATEGORIES: ReadonlyArray<{
  name: string;
  description: string;
  icon: string;
}> = [
${categories.map((c) => `  { name: ${JSON.stringify(c.name)}, description: ${JSON.stringify(c.description)}, icon: ${JSON.stringify(c.icon)} },`).join("\n")}
];
`;

const out = header + tagsBlock + catsBlock;

if (doWrite) {
  const outPath = path.resolve(scriptDir, "../src/data/defaultVaultTemplates.ts");
  fs.writeFileSync(outPath, out, "utf-8");
  console.error(`Wrote ${outPath} (${tags.length} tags, ${categories.length} categories).`);
} else {
  process.stdout.write(out);
}

process.exit(0);
